char	*ft_rot42(char *str)
{
	int		i;
	char	a;

	i = 0;
	while (str[i] != '\0')
	{
		a = str[i];
		if (a >= 'A' && a <= 'Z')
		{
			if (a > 'K')
				a -= 10;
			else
				a += 16;
		}
		else if (a >= 'a' && a <= 'z')
		{
			if (a > 'k')
				a -= 10;
			else
				a += 16;
		}
		str[i] = a;
		i++;
	}
	return (str);
}
