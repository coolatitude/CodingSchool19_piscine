void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int t;

	i = 0;
	while (++i < size)
	{
		if (tab[i] < tab[i - 1])
		{
			t = tab[i];
			tab[i] = tab[i - 1];
			tab[i - 1] = t;
			if (i > 1)
				i -= 2;
		}
	}
}
