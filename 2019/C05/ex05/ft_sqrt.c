int		ft_sqrt(int nb)
{
	int i;

	if (nb < 2)
		return (nb == 1 ? 1 : 0);
	i = 1;
	while (i <= (nb / 2 + nb / 3))
	{
		if (i * i == nb)
			return (i);
		i += 1;
	}
	return (0);
}
