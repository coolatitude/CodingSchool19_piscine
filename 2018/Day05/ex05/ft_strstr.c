char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int ib;

	i = 0;
	while (str[i] != '\0')
	{
		ib = 0;
		while (str[i + ib] == to_find[ib])
		{
			if (to_find[ib + 1] == '\0')
				return (str + i);
			ib++;
		}
		i++;
	}
	return (0);
}
