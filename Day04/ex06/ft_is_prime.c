int	ft_is_prime(int nb)
{
	int i;

	if (nb == 1 || nb == 0)
		return (0);
	i = 1;
	if (nb % 2 == 0)
		return (0);
	while (++i < nb)
		if (nb % i == 0)
			return (0);
	return (1);
}
